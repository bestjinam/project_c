#include <stdio.h>

int	main(void)
{
	int		a;
	int		b;
	int		c;
	double	res;

	scanf("%d", &a);
	scanf("%d", &b);
	scanf("%d", &c);
	res = ((a + b + c) / 3.0);
	res = res * 100;
	res += 0.5;
	res = ((int)res) / (double)100;
	printf("%lf\n", res);
}
