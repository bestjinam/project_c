#include <stdio.h>

int	main(void)
{
	int	n;
	int	i;
	int	cnt;

	scanf("%d", &n);
	i = 0;
	cnt = 0;
	while (i <= n)
	{
		if ((i / 10 > i % 10) && i % 10 != 0)
		{
			printf("%d\n", i);
			cnt ++;
		}
		i ++;
	}
	printf("%d\n", cnt);
	return (0);
}
