#include <stdio.h>

int	main(void)
{
	int	a;
	int	idx;
	int	cnt;

	scanf("%d", &a);
	idx = 1;
	cnt = 0;
	while (a-- > 0)
	{
		if (idx++ % 2 == 0)
			cnt ++;
	}
	printf("%d\n", cnt);
	return (0);
}
