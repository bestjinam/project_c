#include <stdio.h>

int	digit(int n)
{
	int	cnt;

	cnt = 0;
	while (n)
	{
		n = n / 10;
		cnt ++;
	}
	return (cnt);
}

int	main(void)
{
	int	n;

	scanf("%d", &n);
	printf("%d\n", digit(n));
}
