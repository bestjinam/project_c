#include <stdio.h>

int	main(void)
{
	int	a;
	int	idx;
	int	sum;

	scanf("%d", &a);
	sum = 0;
	idx = 1;
	while (a-- > 0)
		sum += idx++;
	printf("%d\n", sum);
	return (0);
}
