#include <stdio.h>

int	main(void)
{
	char	str[10000];
	int		idx;

	scanf("%s", str);
	idx = 0;
	while (*(str + idx))
	{
		if (*(str + idx) == 'A')
			*(str + idx) = '#';
		idx ++;
	}
	printf("%s\n", str);
	return (0);
}
