#include <stdio.h>

int	main(void)
{
	int	a;
	int	b;
	int	c;
	int	min;

	scanf("%d %d %d", &a, &b, &c);
	min = a;
	if (min >= b)
		min = b;
	if (min >= c)
		min = c;
	printf("%d\n", min);
	return (0);
}
