#include <stdio.h>

int	main(void)
{
	int	a[7];
	int	i;
	int	min;

	i = 0;
	min = 2147483647;
	while (i < 7)
	{
		scanf("%d", &a[i]);
		if (min > a[i])
			min = a[i];
		i ++;
	}
	printf("%d\n", min);
	return (0);
}
