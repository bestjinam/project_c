#include <stdio.h>

int	main(void)
{
	int	n;
	int	i;
	int	j;

	scanf("%d", &n);
	i = 1;
	while (i <= n)
	{
		j = 1;
		while (j <= n)
		{
			if (j != i)
				printf("%d%d\n", i, j);
			j ++;
		}
		i ++;
	}
	return (0);
}
