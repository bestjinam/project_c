#include <stdio.h>

int	main(void)
{
	int	a;
	int	b;
	int	res;

	scanf("%d", &a);
	scanf("%d", &b);
	if (a > b)
		res = b;
	else
		res = a;
	printf("%d\n", res);
	return (0);
}
