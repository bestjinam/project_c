#include <stdio.h>

int	main(void)
{
	int	i;
	int	j;
	int	n;

	scanf("%d", &n);
	i = 1;
	while (i <= 6)
	{
		j = 1;
		while (j <= 6)
		{
			if (i + j == n)
				printf("%d %d\n", i, j);
			j ++;
		}
		i ++;
	}
	return (0);
}
