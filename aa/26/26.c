#include <stdio.h>

int	main(void)
{
	int	a;
	int	b;
	int	idx;

	scanf("%d %d", &a, &b);
	idx = 0;
	while (idx <= a)
	{
		if (2 * idx == b - 2 * a)
			break ;
		idx ++;
	}
	printf("%d %d\n", idx, a - idx);
	return (0);
}
