#include <stdio.h>

int	main(void)
{
	int	a;
	int	idx;

	scanf("%d", &a);
	idx = 1;
	while (a -- > 0)
	{
		if (a % idx == 0)
			printf("%d ", idx);
		idx ++;
	}
	return (0);
}
