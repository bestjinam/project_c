#include <stdio.h>

int	main(void)
{
	int	a;
	int	idx;

	scanf("%d", &a);
	idx = 1;
	while (a-- > 0)
	{
		if (idx % 2 != 0)
			printf("%d\n", idx);
		idx++;
	}
	return (0);
}
